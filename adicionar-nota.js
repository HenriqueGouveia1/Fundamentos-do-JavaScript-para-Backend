
//Variável constante com array contendo 3 elementos
const notas = [10, 6, 8]

//utilizando a função push para adicionar um elemento no final do array "notas"
notas.push(7)

//imprimindo o array notas após modificar
console.log(notas)

//criando constante "media" para fazer a media entre cada elemento do array "notas" e dividindo pelo tamanho do array "notas"
const media = (notas[0]+notas[1]+notas[2]+notas[3])/notas.length

//imprimindo a "media"
console.log(media)